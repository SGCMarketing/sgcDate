const DayFunctions = require('./dayFunctions')
const MonthFunctions = require('./monthFunctions')
const YearFunctions = require('./yearFunctions')

class Month {
    constructor(date) {
        this.date = date
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    firstDayOfMonth() {
        var d = new Date(this.date)
        return new Date(d.getFullYear(), d.getMonth(), 1, 0, 0, 0)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    firstSundayOfMonth() {
        return MonthFunctions.firstXOfMonth(this.firstDayOfMonth(), 0)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    firstMondayOfMonth() {
        return MonthFunctions.firstXOfMonth(this.firstDayOfMonth(), 1)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    firstTuesdayOfMonth() {
        return MonthFunctions.firstXOfMonth(this.firstDayOfMonth(), 2)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    firstWednesdayOfMonth() {
        return MonthFunctions.firstXOfMonth(this.firstDayOfMonth(), 3)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    firstThursdayOfMonth() {
        return MonthFunctions.firstXOfMonth(this.firstDayOfMonth(), 4)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    firstFridayOfMonth() {
        return MonthFunctions.firstXOfMonth(this.firstDayOfMonth(), 5)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    firstSaturdayOfMonth() {
        return MonthFunctions.firstXOfMonth(this.firstDayOfMonth(), 6)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    lastDayOfMonth() {
        var d = this.firstDayOfMonth(d)
        d.setMonth(d.getMonth() + 1)
        d.setDate(d.getDate() - 1)
        return new Date(d.getFullYear(), d.getMonth(), d.getDate(), 23, 59, 59, 99)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    startOfDay() {
        var d = new Date(this.date)
        return DayFunctions.startOfDay(d)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    endOfDay() {
        var d = new Date(this.date)
        return DayFunctions.endOfDay(d)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    firstDayOfYear() {
        var d = new Date(this.date)
        return YearFunctions.firstDayOfYear(d)
    }

    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    lastDayOfYear() {
        var d = new Date(this.date)
        return YearFunctions.lastDayOfYear(d)
    }
}

module.exports = Month
